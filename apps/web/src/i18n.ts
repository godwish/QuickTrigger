const translations: Record<SupportedLanguage, Record<TranslationKey, string>> = {
  ko: koreanMessages,
  en: englishMessages,
  ja: japaneseMessages,
  "zh-CN": simplifiedChineseMessages
};

function readStoredLanguage() {
  if (typeof window === "undefined") {
    return "";
  }

  return window.localStorage.getItem(LANGUAGE_STORAGE_KEY) ?? "";
}

function detectBrowserLanguage() {
  if (typeof navigator === "undefined") {
    return "ko";
  }

  return navigator.language;
}

export function resolveLanguage(input?: string | null): SupportedLanguage {
  const value = (input ?? "").trim().toLowerCase();

  if (value === "en" || value.startsWith("en-")) {
    return "en";
  }

  if (value === "ja" || value.startsWith("ja-")) {
    return "ja";
  }

  if (value === "zh-cn" || value === "zh" || value.startsWith("zh-")) {
    return "zh-CN";
  }

  return "ko";
}

function getInitialLanguage() {
  const stored = readStoredLanguage();

  if (stored) {
    return resolveLanguage(stored);
  }

  return resolveLanguage(detectBrowserLanguage());
}

type LocaleState = {
  language: SupportedLanguage;
  setLanguage: (language: string, persist?: boolean) => void;
};

export const useLocaleStore = create<LocaleState>((set) => ({
  language: getInitialLanguage(),
  setLanguage: (language, persist = true) => {
    const nextLanguage = resolveLanguage(language);

    if (typeof window !== "undefined" && persist) {
      window.localStorage.setItem(LANGUAGE_STORAGE_KEY, nextLanguage);
    }

    set({ language: nextLanguage });
  }
}));

export function getCurrentLanguage() {
  return useLocaleStore.getState().language;
}

export function translate(
  language: string | null | undefined,
  key: TranslationKey,
  params?: Record<string, string | number>
) {
  const locale = resolveLanguage(language);
  const template = translations[locale][key] ?? translations.ko[key] ?? key;

  return template.replace(/\{(\w+)\}/g, (_match, token: string) =>
    params?.[token] === undefined ? "" : String(params[token])
  );
}

export function useI18n() {
  const language = useLocaleStore((state) => state.language);
  const setLanguage = useLocaleStore((state) => state.setLanguage);
  const t = useCallback(
    (key: TranslationKey, params?: Record<string, string | number>) => translate(language, key, params),
    [language]
  );

  return {
    language,
    setLanguage,
    t,
    languageOptions: LANGUAGE_OPTIONS
  };
}

export function getLanguageOptionLabel(language: SupportedLanguage) {
  const option = LANGUAGE_OPTIONS.find((item) => item.value === language) ?? LANGUAGE_OPTIONS[0];
  return `${option.flag} ${option.label}`;
}

export function getRoleLabel(language: string | null | undefined, role: "user" | "manager" | "admin") {
  return translate(language, `role.${role}` as TranslationKey);
}
